def tile_to_item(tile, u, v, noexcept=False):
    "Returns an item equivalent to the tile passed"
    known = { }
    for gem in ('Sapphire', 'Ruby', 'Emerald', 'Topaz', 'Amethyst', 'Diamond'):
        known[Tiles[gem]] = Items[gem]
    lookups = {
        Tiles['ExposedGems']: _identify_ExposedGems,
        Tiles['SmallPiles']: _identify_SmallPiles,
        Tiles['DyePlants']: _identify_DyePlants,
        Tiles['Banners']: _identify_Banners
    }
    if tile in known:
        return known[tile]
    if tile in lookups:
        return lookups[tile](tile, u, v)
    if not noexcept:
        raise ValueError("Tile %s not known" % (tile,))
    return INVALID

def strtoint(string, base):
    if base is None:
        return int(string, base=0)
    return int(string, base)

def main_normal(Tables, args):
    fmt = "%s %s" if not args.X else "0x%x %s"
    base = None
    if args.x:
        base = 16
    elif args.d:
        base = 10
    elif args.o:
        base = 8
    for idstr in args.keys:
        idnr = 0
        try:
            idnr = strtoint(idstr, base)
            print(fmt % (idnr, Tables[args.table][idnr]))
        except ValueError as e:
            print("%s <invalid number: %s>" % (idnr, e))
            if not args.f:
                raise
        except KeyError as e:
            print("%s <entry not found>" % (idnr,))
            if not args.f:
                raise

def main_search(Tables, args):
    fmt = "%s %s" if not args.X else "0x%x %s"
    for key in args.keys:
        found = False
        for idnr, value in Tables[args.table].iteritems():
            if key.lower() in value.lower():
                found = True
                print(fmt % (idnr, value))
        if not found:
            print(fmt % (key, "<entry not found>"))

